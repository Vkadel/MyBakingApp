//Attention: This Test is only for tablet configurations
@LargeTest
@RunWith(AndroidJUnit4.class)
public class RecipeListActivityCheckTablet {

    @Rule
    public ActivityTestRule<RecipeListActivity> mActivityTestRule = new ActivityTestRule<>(RecipeListActivity.class);

    @Test
    public void recipeListActivityCheckTablet() {
        ViewInteraction recyclerView = onView(
                Matchers.allOf(ViewMatchers.withId(R.id.recipe_list),
                        childAtPosition(
                                withClassName(is("android.widget.LinearLayout")),
                                0)));
        recyclerView.perform(actionOnItemAtPosition(2, click()));
        onView(withId(R.id.playerView)).check(matches(isDisplayed()));
        onView(withId(R.id.recipe_detail_ingredients)).check(matches(isDisplayed()));
        onView(withId(R.id.recipe_detail_ingredients)).check(matches(not("")));
    }

    private static Matcher<View> childAtPosition(
            final Matcher<View> parentMatcher, final int position) {

        return new TypeSafeMatcher<View>() {
            @Override
            public void describeTo(Description description) {
                description.appendText("Child at position " + position + " in parent ");
                parentMatcher.describeTo(description);
            }

            @Override
            public boolean matchesSafely(View view) {
                ViewParent parent = view.getParent();
                return parent instanceof ViewGroup && parentMatcher.matches(parent)
                        && view.equals(((ViewGroup) parent).getChildAt(position));
            }
        };
    }
}
